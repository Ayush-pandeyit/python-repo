# IF with ELSE

age = 15
if age >= 18:
    print("You can vote!")
else:
    print("You're too young to vote")

# IF with ELIF (else if)

score = 85

if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: F")
