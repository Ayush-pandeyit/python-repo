# Finding the Index of a Tuple Item
tup1 = (25, 12, 34, 10, -32, 10)
print("Tup1: ", tup1)
x = tup1.index(10)
print("First index of 10 : ", x)


# Counting Tuple Items
tup1 = (10, 20, 30, 10, 35, 40, 10)
print("tup1 : ", tup1)
c = tup1.count(10)
print("count of 10 :", c)


tup1 = (10, 20 / 80, 0.25, 10 / 40, 30, 10, 55)
print("Tup1:", tup1)
c = tup1.count(0.25)
print("count of 0.25:", c)
