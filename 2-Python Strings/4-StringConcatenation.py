# String Concatenation using '+' operators

str1 = "Hello"
str2 = "World"
print("string 1:", str1)
print("string 2:", str2)
str3 = str1 + str2
print("String 3:", str3)


# Concatenating String with space

str1 = "Hello"
str2 = "World"
blank = " "
print("string1 :", str1)
print("string2 :", str2)
str3 = str1 + blank + str2
print("string3 :", str3)

# String Concatenation By Multiplying

newstring = "Hello " * 4
print(newstring)

# String Concatenation With '+' and '*' Operators

str1 = "Hello"
str2 = "World"
print("string1 :", str1)
print("string2 :", str2)
str3 = (str1 + str2) * 3
print("string3 :", str3)
